package House;

public class Ceiling {
    private int height;
    private int pantedColor;

    public Ceiling(int height, int pantedColor) {
        this.height = height;
        this.pantedColor = pantedColor;
    }

    public int getHeight() {
        return height;
    }

    public int getPantedColor() {
        return pantedColor;
    }
}
